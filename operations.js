// ADD or CONCATENATION
console.log(31 + 3.5);
console.log("Hello" + "world!");

// SUBTRACT
console.log(2017 - 1969);

// MULTIPLY
console.log(4 * 4);

// DIVIDE
console.log(50 / 5);

// MODULE DIVISION
console.log(14 % 3); // 2
console.log(99 % 8); // 3
console.log(11 % 3); // 2

// IDENTITY OPERATION
var myVariable = 3;
myVariable === 4;

// NEGATION OR NOT EQUAL
var myVariable = 3;
!(myVariable === 3);

// COMPARISON
console.log(15 > 4); // TRUE
console.log("Xiao Hui".length < 122); //TRUE
console.log("Goody Donaldson".length > 8); // TRUE
console.log(8*2 === 16); // TRUE
console.log(10*1 === 1); // FALSE
console.log(30 < 20); // FALSE
